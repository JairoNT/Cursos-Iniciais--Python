
num_1 = int(input('Primeiro número: '))
num_2 = int(input('Segundo número: '))

# Adição
print('{} + {} = '.format(num_1, num_2))
print(num_1 + num_2)

# Subtração
print('{} - {} = '.format(num_1, num_2))
print(num_1 - num_2)

# Multiplicação
print('{} * {} = '.format(num_1, num_2))
print(num_1 * num_2)

# Divisão
print('{} / {} = '.format(num_1, num_2))
print(num_1 / num_2)
